async function updateArticles() {
  const response = await fetch('https://psicologiadireta.com.br/api/blog?all=true');
  const data = await response.json();
  const existingPosts = data.posts;
  
  for (let i = 0; i < articles.length && i < existingPosts.length; i++) {
    const article = articles[i];
    const existingPost = existingPosts[i];
    
    try {
      const updateResponse = await fetch(`https://psicologiadireta.com.br/api/blog/${existingPost.id}`, {
        method: 'PUT',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          title: article.title,
          category: article.category,
          image: article.image,
          content: article.content
        })
      });
      const result = await updateResponse.json();
      if (result.success) {
        console.log(`OK: ${article.title} [${article.category}]`);
      } else {
        console.log(`ERRO: ${article.title} - ${result.error}`);
      }
    } catch (error) {
      console.log(`ERRO: ${article.title} - ${error.message}`);
    }
  }
}

